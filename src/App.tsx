import { useEffect, useState } from "react";
import type { Schema } from "../amplify/data/resource";
import { generateClient } from "aws-amplify/data";

const client = generateClient<Schema>();

function App() {
  const [todos, setTodos] = useState<Array<Schema["Todo"]["type"]>>([]);

  useEffect(() => {
    client.models.Todo.observeQuery().subscribe({
      next: (data) => setTodos([...data.items]),
    });
  }, []);

  function createTodo() {
    let promptContent = window.prompt("Todo content")
    if(promptContent){
      client.models.Todo.create({ content: promptContent});
    }
    else{
      window.alert('please enter value')
    }
  }

  return (
    <main>
      <h1>Zen todos</h1>
      <button onClick={createTodo}>+ new</button>
      <ul>
        {todos.map((todo) => (
          <li key={todo.id}>{todo.content}</li>
        ))}
      </ul>
      <div>
        ðŸ¥³ App successfully hosted. Try creating a new todo.
        <br />
{/*         <a href="https://next-release-dev.d1ywzrxfkb9wgg.amplifyapp.com/react/start/quickstart/vite-react-app/#step-2-add-delete-to-do-functionality">
          Review next step of this tutorial.
        </a> */}
      </div>
    </main>
  );
}

export default App;
